package sanghavi;

public class SwapingTwoNumbers {

	public static void main(String[] args) {
		int a=10,b=5;
		System.out.println("Before swapping");
		System.out.println(a);
		System.out.println(b);
		int temp=a;
		a=b;
		b=temp;
		System.out.println("After swapping");
		System.out.println(a);
		System.out.println(b);
	}

}
